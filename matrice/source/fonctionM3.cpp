void addittionm(float m1[3][3],float m2[3][3],float r[3][3]){
    r[0][0]=m1[0][0]+m2[0][0];
    r[0][1]=m1[0][1]+m2[0][1];
    r[0][2]=m1[0][2]+m2[0][2];
    r[1][0]=m1[1][0]+m2[1][0];
    r[1][1]=m1[1][1]+m2[1][1];
    r[1][2]=m1[1][2]+m2[1][2];
    r[2][0]=m1[2][0]+m2[2][0];
    r[2][1]=m1[2][1]+m2[2][1];
    r[2][2]=m1[2][2]+m2[2][2];
}
void soustractionm(float m1[3][3],float m2[3][3],float r[3][3]){
   r[0][0]=m1[0][0]-m2[0][0];
    r[0][1]=m1[0][1]-m2[0][1];
    r[0][2]=m1[0][2]-m2[0][2];
    r[1][0]=m1[1][0]-m2[1][0];
    r[1][1]=m1[1][1]-m2[1][1];
    r[1][2]=m1[1][2]-m2[1][2];
    r[2][0]=m1[2][0]-m2[2][0];
    r[2][1]=m1[2][1]-m2[2][1];
    r[2][2]=m1[2][2]-m2[2][2];
}
void produitm(float m1[3][3],float m2[3][3],float r[3][3]){
    r[0][0]=(m1[0][0]*m2[0][0])+(m1[0][1]*m2[1][0])+(m1[0][2]*m2[2][0]);
    r[0][1]=(m1[0][0]*m2[0][1])+(m1[0][1]*m2[1][1])+(m1[0][2]*m2[2][1]);
    r[0][2]=(m1[0][0]*m2[0][2])+(m1[0][1]*m2[1][2])+(m1[0][2]*m2[2][2]);
    r[1][0]=(m1[1][0]*m2[0][0])+(m1[1][1]*m2[1][0])+(m1[1][2]*m2[2][0]);
    r[1][1]=(m1[1][0]*m2[0][1])+(m1[1][1]*m2[1][1])+(m1[1][2]*m2[2][1]);
    r[1][2]=(m1[1][0]*m2[0][2])+(m1[1][1]*m2[1][2])+(m1[1][2]*m2[2][2]);
    r[2][0]=(m1[2][0]*m2[0][0])+(m1[2][1]*m2[1][0])+(m1[2][2]*m2[2][0]);
    r[2][1]=(m1[2][0]*m2[0][1])+(m1[2][1]*m2[1][1])+(m1[2][2]*m2[2][1]);
    r[2][2]=(m1[2][0]*m2[0][2])+(m1[2][1]*m2[1][2])+(m1[2][2]*m2[2][1]);
    
}
void transposerm(float m1[3][3],float r[3][3]){
    r[0][0]=m1[0][0];
    r[0][1]=m1[1][0];
    r[0][2]=m1[2][0];
    r[1][0]=m1[0][1];
    r[1][1]=m1[1][1];
    r[1][2]=m1[2][1];
    r[2][0]=m1[0][2];
    r[2][1]=m1[1][2];
    r[2][2]=m1[2][2];
}

float determinantm(float m1[3][3]){
int i=0;
i=(m1[0][0]*m1[1][1]*m1[2][3])+(m1[0][1]*m1[1][2]*m1[2][0])-(m1[0][2]*m1[1][0]*m1[2][1]);
return i;
}

float tracem(float m1[3][3]){
int i;
i=m1[0][0]+m1[1][1]+m1[2][2];
return i;
}

void inversem(float m1[3][3],float r[3][3]){
int i=determinantm(m1);
transposerm(m1,r);
r[0][0]=-1*r[0][0];
r[0][1]=r[0][1];
r[0][2]=-1*r[0][2];
r[1][0]=r[1][0];
r[1][1]=-1*r[1][1];
r[1][2]=r[1][2];
r[2][0]=-1*r[2][0];
r[2][1]=r[2][1];
r[2][2]=-1*r[2][2];
r[0][0]*=i;
r[0][1]*=i;
r[0][2]*=i;
r[1][0]*=i;
r[1][1]*=i;
r[1][2]*=i;
r[2][0]*=i;
r[2][1]*=i;
r[2][2]*=i;
}